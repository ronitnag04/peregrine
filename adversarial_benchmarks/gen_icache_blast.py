#!/usr/bin/env python3
"""
Generates icache_blast_gen.h containing N distinct non-inlinable "leaf"
functions and an array of pointers to them. Goal: make the static code
footprint of icache_blast exceed a typical L1I so the frontend becomes
the bottleneck -- a situation invisible to the ronamol feature set,
which measures only the dynamic instruction mix.

Tune N_FUNCS and PAD so that N_FUNCS * (bytes-per-func) crosses at
least 64 KiB, comfortably beyond the 32 KiB L1I found on most
Cortex-A / x86 cores in the peregrine sweep.
"""

import sys

N_FUNCS = 512       # number of distinct callee bodies
PAD_OPS = 12        # ALU ops per callee -- enough to push each body into
                    # its own cache line (~32-48 bytes when -O2)

out = []
out.append("/* AUTO-GENERATED by gen_icache_blast.py -- do not edit. */")
out.append("#ifndef ICACHE_BLAST_GEN_H")
out.append("#define ICACHE_BLAST_GEN_H")
out.append("#include <stdint.h>")
out.append("")

# Each function is distinct (different constants) to defeat linker
# folding, but has the same dynamic "signature": a handful of int ALU
# ops and a return.
for i in range(N_FUNCS):
    c1 = (i * 2654435761) & 0xFFFFFFFF
    c2 = (i * 40503) & 0xFFFF
    ops = [f"x ^= {c1:#x}u;"]
    for k in range(PAD_OPS):
        ops.append(f"x = x * {((c2 + k) | 1):#x}u + {((c1 + k) | 1):#x}u;")
    body = " ".join(ops)
    out.append(
        f"__attribute__((noinline)) static uint32_t fn_{i}(uint32_t x) {{ "
        f"{body} return x; }}"
    )

out.append("")
out.append(f"#define ICACHE_BLAST_N_FUNCS {N_FUNCS}")
out.append("typedef uint32_t (*fn_ptr_t)(uint32_t);")
out.append("static const fn_ptr_t fn_table[ICACHE_BLAST_N_FUNCS] = {")
for i in range(N_FUNCS):
    out.append(f"    fn_{i},")
out.append("};")
out.append("")
out.append("#endif")
print("\n".join(out))
